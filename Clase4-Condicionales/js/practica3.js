let traductor = "mesa";

switch(traductor) {
    case "perro" :
        console.log("dog");
        break;
    case "gato" :
        console.log("cat");
        break;
    case "ventana" :
        console.log("window");
        break;
    case "mesa" : 
        console.log("table");
        break;
    default : 
        console.log("la palabra ingresada es incorrecta.");
        break;
}